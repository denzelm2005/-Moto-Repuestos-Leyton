 public void obtenerDatos() throws SQLException {
        List<Proveedor> proveedores = new DAOProveedor().ObtenerDatos();
        DefaultTableModel modelo = new DefaultTableModel();
        String[] columnas = {"ID_Prov", "Nombre","Empresa","Teléfono"};
        modelo.setColumnIdentifiers(columnas);
        for(Proveedor au : proveedores){
             String[] renglon = {Integer.toString(au.getID_Prov()), au.getNombre()
             ,au.getEmpresa(),au.getTeléfono()};
             modelo.addRow(renglon);
          }
        jTableProveedor.setModel(modelo);   
    } 
 
  public void  actualizarProveedor() throws SQLException {
     int  id = Integer.parseInt(this.jTextID_Prov.getText());
     String nom = this.jTextNombres.getText();
     String emp = this.jTextEmpresa.getText();
     String tel = this.jFormattedTelefono.getText();

     Proveedor prov = new Proveedor(id, nom, tel, emp);
     
     DAOProveedor dao = new DAOProveedor();
     int res = dao.Actualizar(prov);
     if (res == 0){
         JOptionPane.showMessageDialog(rootPane,
                 "Proveedor Actualizado" );
     }else{
         JOptionPane.showMessageDialog(rootPane, 
                 "¡Ocurrio un Error!");
         
     }
    }
 
