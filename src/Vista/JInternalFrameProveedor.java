    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int fila = this.jTableProveedor.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(rootPane, 
                    "Seleccione un registro de la tabla");
            
        }else{
           try{
               int id = Integer.parseInt((String) this.jTableProveedor.
                       getValueAt(fila, 0).toString());
               String nom = (String) this.jTableProveedor.getValueAt(fila, 1);
               String tel = (String) this.jTableProveedor.getValueAt(fila, 2);
               String emp = (String) this.jTableProveedor.getValueAt(fila, 3);
               
              
               
               jTextID_Prov.setText("" + id);
               jTextNombres.setText(nom);
               jFormattedTelefono.setText(emp);
               jTextEmpresa.setText(tel);
           
             
               
           } catch (NumberFormatException e){
               JOptionPane.showMessageDialog(rootPane, 
                       "¡Ocurrio un error!"+e.getMessage());
           }
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

