    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
     try {
           
           String nom =jTextNombres.getText();
           String Em = jTextEmpresa.getText();
           String Tel = jFormattedTelefono.getText();
         
           
           if (nom.contentEquals("") || Em.contentEquals("")
               || Tel.contentEquals("")){
               JOptionPane.showMessageDialog(rootPane, 
                       "Todos los campos son obligatorios");
               
           }else {
               try {
                  
                   Proveedor auto = new Proveedor(nom, Tel, Em);
                   DAOProveedor dao = new DAOProveedor();
                   if (dao.Insertar(auto) == 0) {
                       JOptionPane.showMessageDialog(rootPane,
                               "Registro agregado");
                   }
                   
               } catch (HeadlessException | SQLException e) {
                   JOptionPane.showMessageDialog(rootPane, 
                           "No se agrego el registro");
               }
           }
           obtenerDatos();
           
           limpiarCampos();
       } catch (SQLException ex) {
          Logger.getLogger(JInternalFrameCliente.class.getName()).
                   log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void jTextNombresKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextNombresKeyTyped
        char car = evt.getKeyChar();
        if ((car < 'a' || car > 'z') && (car <'A' || car > 'Z')
            && car != 'á' //Minuscula
            && car != 'é'
            && car != 'í'
            && car != 'ó'
            && car != 'ú'
            && car != 'Á'
            && car != 'É'
            && car != 'Í'
            && car != 'Ó'
            && car != 'Ú'
            && car != 'Ü'
            && car != 'ü'
            && car != 'Ñ'
            && car != 'ñ'
            && (car != (char) KeyEvent. VK_SPACE)) {
            evt.consume();
        }
    }//GEN-LAST:event_jTextNombresKeyTyped

